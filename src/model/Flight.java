package model;

public final class Flight {
    /* /////////////////////////////////////////////////////////////////////////////// */
    /* --------------------------------- ATTRIBUTS ----------------------------------- */
    /* /////////////////////////////////////////////////////////////////////////////// */

    public int Id;
    public String Icao;
    public float Spd;
    public float Trak;
    public String Type;
    public float Lat;
    public float Long;

    /* /////////////////////////////////////////////////////////////////////////////// */
    /* ------------------------------- CONSTRUCTEURS --------------------------------- */
    /* /////////////////////////////////////////////////////////////////////////////// */

    /* /////////////////////////////////////////////////////////////////////////////// */
    /* --------------------------------- METHODES- ----------------------------------- */
    /* /////////////////////////////////////////////////////////////////////////////// */

    @Override
    public String toString() {
        return "FLIGHT " + Id + " - Icao :" + Icao + " - Speed: " + Spd + " - Trak: " + Trak + " - Type: " + Type
                + " - Lat: " + Lat + " - Long: " + Long;
    }
}
