package model;

public final class Flight {
    /* /////////////////////////////////////////////////////////////////////////////// */
    /* --------------------------------- ATTRIBUTS ----------------------------------- */
    /* /////////////////////////////////////////////////////////////////////////////// */

    public int Id;
    public String Icao;
    public float Spd;
    public float Trak;
    public String Type;
    public float Lat;
    public float Long;
    public String From;
    public String To;

    /* /////////////////////////////////////////////////////////////////////////////// */
    /* ------------------------------- CONSTRUCTEURS --------------------------------- */
    /* /////////////////////////////////////////////////////////////////////////////// */

    /* /////////////////////////////////////////////////////////////////////////////// */
    /* --------------------------------- METHODES- ----------------------------------- */
    /* /////////////////////////////////////////////////////////////////////////////// */

    // Override ----------------------------------------------

    @Override
    public String toString() {
        return "FLIGHT NUMBER " + Id + ", ICAO:" + "\nLAT: " + Lat + ", LON: " + Long;
    }
}
